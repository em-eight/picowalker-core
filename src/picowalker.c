void walker_entry() {

	// Setup IR uart and rx interrupts
	setup_ir_uart();
	setup_buttons();

    pw_screen_init();


    pw_img_t dowsing = {
            width: 80,
            height: 16,
            data: dowsing_bin,
            size: dowsing_bin_len,
    };
    pw_screen_draw_img(&dowsing, 0, 0);

	// init accel

	// Event loop
	while(1) {
		// Check steps
		// Switch state

		gpio_put(LED_PIN, 1);
		sleep_ms(250);
		gpio_put(LED_PIN, 0);
		sleep_ms(250);

	}

}



